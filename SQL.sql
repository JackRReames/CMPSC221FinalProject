DROP TABLE ORDERS;
DROP TABLE PRODUCTS;
DROP TABLE CUSTOMERS;

CREATE TABLE PRODUCTS (
    ID INT NOT NULL,
    PRODNAME VARCHAR(255),
    PRICE DOUBLE,
    INVENTORY INT,
    PRIMARY KEY(ID)
);

