CREATE TABLE ORDERS (
    ID INT NOT NULL,
    ORDERTIME BIGINT,
    AMOUNT INT,
    PRODID INT,
    CUSTID INT,
    PRIMARY KEY (ID),
    FOREIGN KEY (PRODID) REFERENCES PRODUCTS(ID),
    FOREIGN KEY (CUSTID) REFERENCES CUSTOMERS(ID)
);

