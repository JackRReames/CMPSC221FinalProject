CREATE TABLE CUSTOMERS (
    ID INT NOT NULL,
    LNAME VARCHAR(255),
    FNAME VARCHAR(255),
    PHONE VARCHAR(255),
    EMAIL VARCHAR(255),
    PRIMARY KEY (ID)
);

